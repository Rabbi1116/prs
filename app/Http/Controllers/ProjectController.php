     */
    public function store(Request $request)
    {
       $projectadd =new Project();
       $projectadd->project_title= $request->project;
       $projectadd->created_at= date('d-m-Y H:i:s');
       $projectadd->updated_at= date('d-m-Y H:i:s');
       $projectadd->save();

     

       return redirect()->back();
    }

